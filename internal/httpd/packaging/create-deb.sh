set -euxo pipefail

SCRIPT_DIR="$( cd "$( dirname ${BASH_SOURCE[0]} )" && pwd )"
. $SCRIPT_DIR/common.sh

ARCH="${2:-amd64}"
# TODO: fix-me later
OUTPUT_DIR="/mnt/host/out-deb"

VERSION="0.0.1"

mkdir -p "$OUTPUT_DIR"

sudo fpm -s dir -t deb -n "$PKG_NAME" -v "$VERSION" -f -p "$OUTPUT_DIR" \
    --vendor "$PKG_VENDOR" \
    --maintainer "$PKG_MAINTAINER" \
    --description "$PKG_DESCRIPTION" \
    --license "$PKG_LICENSE" \
    --url "$PKG_URL" \
    --architecture "$ARCH" \
    --deb-dist "stable" \
    --deb-use-file-permissions \
    --after-install "$POSTINSTALL_PATH" \
    --before-remove "$PREUNINSTALL_PATH" \
    "${SCRIPT_DIR}/../../../httpd/config/=/etc/apache2/mods-available/" \
    "${SCRIPT_DIR}/../../../mod-otel.so=/usr/lib/apache2/modules/mod_otel.so"
